Променливата $intCount не е инициализирана. Трябва да зададем стойност, за да контролираме колко пъти ще се изпълнява цикъла.
Използване на грешни кавички в асоциативния масив. В PHP ключовете трябва да бъдат в кавички.
Грешен синтаксис за дефиниране на масив. В PHP се използват квадратни скоби или функцията array().
Променлива трябва да се инициализира с for с малка буква.
Форматирането и синтаксиса за конкатенация в echo.
<?php
$intCount = 2; 
$arrFirstSurname = array("Simon" => "Jones", "James" => "Smith", "Alan" => "Barclay", "Gema" => "West");
for($intA = 0; $intA <= $intCount; $intA++) {
    echo $arrFirstSurname["Alan"] . " ";
}
?>
Инициализираме променливата $intCount със стойност 2, за да се изпълни цикълът три пъти.
Ключовете на асоциативния масив са в кавички.
Използваме малка буква за цикъла for.
Конкатенираме коректно с . в echo.
Това би трябвало да работи коректно и да покаже фамилията на „Алан“ три пъти.